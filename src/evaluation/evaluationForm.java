private String generateUniqueFileName(String baseFileName) {
    // Specify the download directory path
    String downloadDirectory = System.getProperty("user.home") + File.separator + "Downloads";

    // Construct the full file path without the extension
    String filePathWithoutExtension = downloadDirectory + File.separator + baseFileName;
    
    // Check if the file already exists
    File file = new File(filePathWithoutExtension + ".pdf");
    int count = 1;
    while (file.exists()) {
        // Append a number to the filename
        filePathWithoutExtension = downloadDirectory + File.separator + baseFileName + count;
        file = new File(filePathWithoutExtension + ".pdf");
        count++;
    }
    
    return filePathWithoutExtension; // Return the final unique file path without extension
}
