    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed

    String B1 = b1.getSelectedItem().toString();
    String B2 = b2.getSelectedItem().toString();
    String B3 = b3.getSelectedItem().toString();
    String B4 = b4.getSelectedItem().toString();
    String B5 = b5.getSelectedItem().toString();

    // Check if any field is empty
    if ( B1.isEmpty() || B2.isEmpty() || B3.isEmpty() || B4.isEmpty() || B5.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        try {
            // Parse the values of A1, A2, A3, A4, and A5 to integers
            int intB1 = Integer.parseInt(B1);
            int intB2 = Integer.parseInt(B2);
            int intB3 = Integer.parseInt(B3);
            int intB4 = Integer.parseInt(B4);
            int intB5 = Integer.parseInt(B5);

            // Calculate the total
            int btotal = (intB1 + intB2 + intB3 + intB4 + intB5) / 5;

            // Create a SQL INSERT statement
            String sql = "INSERT INTO evaluations ( b1, b2, b3, b4, b5, btotal) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement statement = (PreparedStatement) con.prepareStatement(sql);

            // Set the values for the prepared statement parameters
            
            statement.setString(1, B1);
            statement.setString(2, B2);
            statement.setString(3, B3);
            statement.setString(4, B4);
            statement.setString(5, B5);
            statement.setInt(6, btotal);

            // Execute the INSERT statement
            int rowsInserted = statement.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Data inserted successfully.");
            }
        } catch (SQLException ex) {
            // Handle any SQL exceptions
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error inserting data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException ex) {
            // Handle any number format exceptions
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error parsing scores: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_savebtnActionPerformed

